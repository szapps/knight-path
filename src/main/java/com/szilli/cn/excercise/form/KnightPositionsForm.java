package com.szilli.cn.excercise.form;

/**
 * The Class KnightPositionsForm.
 */
public class KnightPositionsForm {

	/** The white knight position. */
	private String whiteKnightPosition;

	/** The black knight position. */
	private String blackKnightPosition;

	/**
	 * Gets the white knight position.
	 *
	 * @return the white knight position
	 */
	public String getWhiteKnightPosition() {
		return whiteKnightPosition;
	}

	/**
	 * Sets the white knight position.
	 *
	 * @param whiteKnightPosition
	 *            the new white knight position
	 */
	public void setWhiteKnightPosition(String whiteKnightPosition) {
		this.whiteKnightPosition = whiteKnightPosition;
	}

	/**
	 * Gets the black knight position.
	 *
	 * @return the black knight position
	 */
	public String getBlackKnightPosition() {
		return blackKnightPosition;
	}

	/**
	 * Sets the black knight position.
	 *
	 * @param blackKnightPosition
	 *            the new black knight position
	 */
	public void setBlackKnightPosition(String blackKnightPosition) {
		this.blackKnightPosition = blackKnightPosition;
	}

}
